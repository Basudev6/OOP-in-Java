
package javadatatype;
/*
Data Type:
a. primitive b. non-primitive
1. byte: smallest type data store: 8 bits, -128 to 127
2. short: 16 bits
3. int: 32 bits
4. long: 64 bits
5. float: 32 bits
6. double: 64 bits
7. character: 16 bits
8. boolean: 1 bits

*/


public class DataTypeCheck {
     
    public static void main(String[]args)
    {
        
        byte b=12;
        short sh = 1234;
        int a = 324452;
        long lighttravel= 10987654443l;
        float pi=3.14159f;
        double d= 876994839.93344344;
        char section ='B';
        boolean bool = false;
        
        System.out.println(b);
        System.out.println(sh);  
        System.out.println(a);
        System.out.println(lighttravel);
        System.out.println(pi);
        System.out.println(d);
        System.out.println(section);
        System.out.println(bool);      
        
    }
}
